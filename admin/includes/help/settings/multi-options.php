<?php if (!defined('IPHORM_VERSION')) exit; ?><h4>Options</h4>
<p>The options are the choices the user has to pick from when using the form.</p>

<h4>Adding an option</h4>
<p>To add an option, click the <img src="<?php echo IPHORM_PLUGIN_URL; ?>/admin/images/small-add-round.png" /> next to
the element and the new option will appear below the this option.</p>

<h4>Deleting an option</h4>
<p>To delete an option, click the <img src="<?php echo IPHORM_PLUGIN_URL; ?>/admin/images/small-delete-round.png" /> next to the option.</p>

<h4>Customize values</h4>
<p>This allows you to have a different value submitted than the value that is displayed to the user
of the form. The label is what the user will see and the value is what is submitted to the form.</p>

<h4>Default values</h4>
<p>The checkboxes in the first column are used to specify the default values. The checkboxes that are
checked here will also be checked when the form is first loaded.</p>